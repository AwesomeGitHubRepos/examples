def printnum(n, d, ox, oy, pad = 0):
    if pad == 0:
        s = str(n)
    else:
        s = '{:02}'.format(n)
    x = ox
    y = oy
    for c in s:
        putdigit(int(c), d, x, y)
        x = x + digit_width + digit_padding
